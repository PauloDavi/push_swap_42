#include "push_swap.h"

void	pb(t_push_swap *p)
{
	ft_printf("pb\n");
	if (p->a_size == 0 || p->b_size == p->total_size)
		return ;
	p->b[p->b_size] = p->a[p->a_size - 1];
	p->b_size++;
	p->a_size--;
}
